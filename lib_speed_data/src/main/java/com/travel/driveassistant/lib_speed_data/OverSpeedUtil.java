    public static OverSpeedResult checkOverSpeed(final @NonNull LatLng userLatLong, final float userSpeed) {
        final OverSpeedResult result = new OverSpeedResult(userLatLong, userSpeed);
        return checkCanalRoadSpeedViolation(userLatLong, userSpeed, result);
    }

    public static OverSpeedResult checkCanalRoadSpeedViolation(final LatLng userLocation, final float userSpeed,
                                                               final @NonNull OverSpeedResult result) {
        // Speed is less than speed limit on this road
        if (userSpeed <= 65) {
            return result;
        }
        boolean isLocationOnPath = PolyUtil.isLocationOnPath(userLocation, getCanalRoadPolyLine(), false, 90);
        if (isLocationOnPath) {
            result.isOverSpeed = true;
            result.addressOfUser = "Canal Road, Lahore";
        }
        return result;
    }

